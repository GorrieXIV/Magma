    data:=[Integers()|];
    for F in Facets(P) do
        bool,fid:=reflexive_dim3_facet_to_id(F);
        if not bool then
            return false,"Argument must be a three-dimensional reflexive polytope";
        end if;
        Append(~data,fid);
    end for;
    Sort(~data);
    bool,id:=reflexive_dim3_ref_list_1(data);
    if bool then return true,id; end if;
    // If we're here then that wasn't enough -- calculate the next set of invars
    D:=Dual(P);
    dualdata:=[Integers()|];
    for F in Facets(D) do
        bool,fid:=reflexive_dim3_facet_to_id(F);
        if not bool then
            return false,"Argument must be a three-dimensional reflexive polytope";
        end if;
        Append(~dualdata,fid);
    end for;
    data cat:= Sort(dualdata);
    bool,id:=reflexive_dim3_ref_list_2(data);
    if bool then return true,id; end if;
    // Still no luck -- try the (expensive) final set...
    bary:=&+Vertices(P) / NumberOfVertices(P);
    data:=EhrhartDeltaVector(P - bary);
    bool,id:=reflexive_dim3_ref_list_3(data);
    if not bool then
        return false,"Argument must be a three-dimensional reflexive polytope";
    end if;
    return true,id;
end function;

