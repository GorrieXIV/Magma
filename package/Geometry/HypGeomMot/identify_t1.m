ARQ:=ArtinRepresentationQuadratic;
NF:=NumberField;
ARs:=ArtinRepresentations;

function odd_part(x)
 _,t:=Valuation(x,2); t:=Numerator(t)*Denominator(t);
 return Sign(t)*&*[Z|f[1]^(f[2] mod 2) : f in Factorization(t)]; end function;

function deg4_t1_wt0_id(H,A,B) y:=PolynomialRing(Rationals()).1;
 if A eq [5] and B eq [1,2,3] then
  return PermutationCharacter(NF(y^3+y^2-3*y+3)); end if;
 if A eq [5] and B eq [1,2,4] then
  return PermutationCharacter(NF(y^3-y^2+2*y+2)); end if;
 if A eq [3,6] and B eq [1,2,4] then
  return PermutationCharacter(NF(y^3+3*y-2)); end if;
 if A eq [8] and B eq [1,2,3] then
  return ARQ(1)+ARs(NF(y^4+2*y^2+3))[5]; end if;
 if A eq [8] and B eq [1,2,4] then
  return Minimize(ARQ(2)+ARs(NF(y^4-2*y^2+2))[5]); end if;
 if A eq [8] and B eq [1,2,6] then T:=ARQ(odd_part(MValue(H)));
  return Minimize(T*deg4_t1_wt0_id(Twist(H),[8],[1,2,3])); end if;
 if A eq [10] and B eq [1,2,6] then T:=ARQ(odd_part(MValue(H)));
  return Minimize(T*deg4_t1_wt0_id(Twist(H),[5],[1,2,3])); end if;
 if A eq [10] and B eq [1,2,4] then T:=ARQ(odd_part(MValue(H)));
  return Minimize(T*deg4_t1_wt0_id(Twist(H),[5],[1,2,4])); end if;
 if A eq [12] and B eq [1,2,4] then R:=ARs(NF(y^6-2*y^3-1));
  R:=[r : r in R | Degree(r) eq 2 and -2 in Eltseq(Character(r))][1];
  return Minimize(ARQ(3)+R); end if;
 if A eq [12] and B eq [1,2,3] then R:=ARs(NF(y^4-4*y-6));
  R:=[r : r in R | Degree(r) eq 3 and not IsOne(Character(Determinant(r)))][1];
  return R; end if;
 if A eq [12] and B eq [1,2,6] then T:=ARQ(odd_part(MValue(H)));
  return Minimize(T*deg4_t1_wt0_id(Twist(H),[12],[1,2,3])); end if;
 return false; end function;

function deg4_t1_id(H,A,B)
 if Weight(H) eq 0 then return deg4_t1_wt0_id(H,A,B); end if;
 if Weight(H) eq 1 then return deg4_t1_wt1_id(A,B); end if;
 if Weight(H) eq 3 then return deg4_t1_wt3_id(A,B); end if;
 return false; end function;

function deg3_t1_id(H,A,B)
 if Weight(H) eq 0 then // weight 0 cases
  if A eq [2,6] and B eq [1,3] then y:=PolynomialRing(Rationals()).1;
   return ArtinRepresentations(NumberField(y^4-2*y^3+3*y^2+4*y+1))[5]; end if;
  if A eq [2,4] and B eq [1,3] then return ARQ(1)+ARQ(-2); end if;
  if A eq [2,6] and B eq [1,4] then return ARQ(-3)+ARQ(6); end if; end if;
 if Weight(H) eq 2 then // weight 2 cases
  if A eq [2,6] and B eq [1,1,1] then // 288a // quartic twist 32a
   K:=QuadraticField(-1); p2:=Factorization(2*Integers(K))[1][1];
   return Grossencharacter(HeckeCharacterGroup(p2^2*3).1^2,[[2,0]]); end if;
  if A eq [2,2,2] and B eq [1,3] then // twist of above by 12,same as 32a
   K:=QuadraticField(-1); p2:=Factorization(2*Integers(K))[1][1];
   return Grossencharacter(HeckeCharacterGroup(p2^2).0,[[2,0]]); end if;
  if A eq [2,4] and B eq [1,1,1] then // 256a
   K:=QuadraticField(-2); G:=HeckeCharacterGroup(1*Integers(K));
   return Grossencharacter(G.0,[[2,0]]); end if;
  if A eq [2,2,2] and B eq [1,4] then // 256a also
   K:=QuadraticField(-2); G:=HeckeCharacterGroup(1*Integers(K));
   return Grossencharacter(G.0,[[2,0]]); end if;
  if A eq [2,3] and B eq [1,1,1] then // 36a
   K:=QuadraticField(-3); G:=HeckeCharacterGroup(2*Integers(K));
   return Grossencharacter(G.0,[[2,0]]); end if;
  if A eq [2,2,2] and B eq [1,6] then // 36a twisted by chi_12
   K:=QuadraticField(-3); psi:=NormInduction(K,KroneckerCharacter(12));
   return Grossencharacter(psi,[[2,0]]); end if;
  if A eq [2,2,2] and B eq [1,1,1] then // 32a
   K:=QuadraticField(-1); p2:=Factorization(2*Integers(K))[1][1];
   return Grossencharacter(HeckeCharacterGroup(p2^2).0,[[2,0]]); end if;
  if A eq [2,3] and B eq [1,4] then // not ec
   K:=QuadraticField(-24); G:=HeckeCharacterGroup(1*Integers(K));
   return Grossencharacter(G.0,[[2,0]]); end if;
  if A eq [2,4] and B eq [1,6] then // twist of prev by Hilb char
   K:=QuadraticField(-24); G:=HeckeCharacterGroup(1*Integers(K));
   return Grossencharacter(G.1,[[2,0]]); end if;
  if A eq [2,3] and B eq [1,6] then // 32a again but twisted by 3
   K:=QuadraticField(-1); psi:=HeckeCharacterGroup(3*Integers(K)).1;
   return Grossencharacter(psi,[[2,0]]); end if; end if;
 return false; end function;

function switch_me(A,B) if 1 in A then return true; end if;
 if 1 in B then return false; end if; return Max(A) gt Max(B); end function;

function identify_t1(H) if Degree(H) eq 1 then return 1; end if;
 A:=H`cycA; B:=H`cycB; if switch_me(A,B) then T:=A; A:=B; B:=T; end if;
 if Degree(H) eq 2 and Weight(H) eq 1 then return 1; end if;
 if Degree(H) eq 2 then
  if A eq [3] and B eq [1,2] then return ARQ(1); end if;
  if A eq [6] and B eq [1,2] then return ARQ(3); end if;
  if A eq [4] and B eq [1,2] then return ARQ(2); end if; end if;
 if Degree(H) eq 3 then return deg3_t1_id(H,A,B); end if;
 if Degree(H) eq 4 then return deg4_t1_id(H,A,B); end if;
 return false; end function;

