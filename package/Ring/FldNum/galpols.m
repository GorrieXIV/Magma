_maxdeg := #_last;

_check_degree := procedure(d)
    error if Type(d) ne RngIntElt, "Degree must be an integer";
    error if d le 1, "Degree must be at least 2";
    error if d gt _maxdeg,
        "Degree must not exceed " cat IntegerToString(_maxdeg);
end procedure;

_check_args := procedure(d, n)
    _check_degree(d);
    error if Type(n) ne RngIntElt, "Label must be an integer";
    error if n lt 1, "Number must be positive";
    error if n gt _last[d] - _last[d - 1],
        "There are only " cat i2s(_last[d] - _last[d - 1]) cat
        " transitive groups of degree " cat i2s(d)
        where i2s is IntegerToString;
end procedure;

_dn_to_i_check := function(d, n)
    _check_args(d, n);
    return _last[d - 1] + n;
end function;


intrinsic PolynomialWithGaloisGroup(d::RngIntElt, n::RngIntElt) -> RngUPolElt
{A polynomial whose Galois group is the n-th transitive group of degree d}
    P := PolynomialRing(Integers());
    return P!_data[_dn_to_i_check(d, n)];
end intrinsic;

