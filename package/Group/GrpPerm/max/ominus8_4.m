function OMinus8_4Identify(group: max:= true, Print:=0)
  A := AutO8m4();
  ims := [A.1, A.2];
  S := sub< A | ims >; //socle of A
  F, phi := FPGroupStrong(S);

  soc := Socle(group);
  iss, gens := StandardGenerators(soc,"O8m4");
  if not iss then error "Standard generator error in O^-(8,4)"; end if;
  a:=gens[1]; b:=gens[2];
  soc := sub< soc | a,b >;
  newgens := [ group | a,b];
  for g in Generators(group) do
     if not g in sub<group|newgens> then Append(~newgens,g); end if;
  end for;
  group := sub<group|newgens>;
  //we need to find images of generators in standard copy
  homom :=  hom< soc -> A | ims >;
  for i in [3..Ngens(group)] do
    g := group.i;
    conjim := homom(soc.1^g);
    isc, h := IsConjugate(A,A.1,conjim);
    if not isc then error "Conjugation error in Aut(O^-(8,4))"; end if;
    CG := Centraliser(A,conjim);
    ce := h;
    isc, h := IsConjugate(CG,A.2^ce,homom(soc.2^g));
    if not isc then error "Conjugation error in Aut(O^-(8,4))"; end if;
    ce := ce*h;
    Append(~ims,ce);
  end for;
  homom :=  hom< group -> A | ims >;
  imhom := Image(homom);
  //we don't need to identify type, since all classes normalised by full aut gp

  maximals:= [];
  if not max then 
    return homom, A, maximals, F, phi;
  end if;

  //Now for the maximals.  We work down the table in the book
  //These are subgroups of OmegaMinus(8,4), so we need a homomorphism
  iss, gens := StandardGenerators(OmegaMinus(8,4),"O8m4");
  om := sub< GL(8,4) | gens >;
  omtoA := hom< om->A | [A.1,A.2] >;
  
  //C1s
  Append(~maximals, omtoA(IsotropicStabOmega(8,4,1,-1)) );
  Append(~maximals, omtoA(IsotropicStabOmega(8,4,2,-1)) );
  Append(~maximals, omtoA(IsotropicStabOmega(8,4,3,-1)) );
  Append(~maximals, omtoA(NSPointStabOmega(8,4,-1)) ); //S_6(4)

  Append(~maximals, omtoA(NonDegenStabOmega(8,4,-1,6,1)) );
  Append(~maximals, omtoA(NonDegenStabOmega(8,4,-1,2,1)) );
  Append(~maximals, omtoA(NonDegenStabOmega(8,4,-1,4,1)) );

  //C3
  Append(~maximals, omtoA(GammaOdimEven(8,4,-1)) );
   
  //C9
  Append(~maximals,omtoA(U34()));

  return homom, A, maximals, F, phi;
end function;
