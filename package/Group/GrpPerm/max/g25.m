get_standard_gens_g25 := function(G)
// Standard gens [(2A,3B) + side condns] from Birmingham web page 19/2/04 
// Assumes G is isomorphic to G2(5)

    P := RandomProcess(G);
    found_a := false;
    found_b := false;
    repeat
	x := Random(P);
	ord := Order(x);

	// Proportion of even orders is 37/64.
	// Only one class of involutions.
	if not found_a and IsEven(ord) then
	    found_a := true;
	    a := x ^ (ord div 2);
	end if;

	// Proportion of (ord mod 3 eq 0 and ord ne 21) is 80/189
	// Note that order 21 elements all power to 3A
	if not found_b and ord mod 3 eq 0 and ord ne 21 then
	    b := x ^ (ord div 3);
	    found_b := true;
	end if;

	if found_a and found_b then
	    // At this point a is in 2A and b is in 3AB.
	    // (b in 3B is a little more likely, 3A:3B is 19:21).
	    // We try to prove b in 3B.
	    ab := a*b;
	    ord := Order(ab);
	    found_b := ord in {3, 4, 5, 7, 15, 21, 24, 25, 31};
	    // If b in 3A, this will be false.
	    // If b in 3B, the proportion of trues is 15296/27125.
	    count := 0;
	    while not found_b and count lt 3 do
		// Keep trying a little longer
		count +:= 1;
		b := b^Random(P);
		ord := Order(ab);
		found_b := ord in {3, 4, 5, 7, 15, 21, 24, 25, 31};
	    end while;
	end if;

    until found_a and found_b; 
    
    // We've got a in 2A and b in 3B, now enforce side conditions
    while not (ord eq 7 and Order(ab^2*b) eq 15) do
        b := b^Random(P);
	ab := a*b;
	ord := Order(ab);
    end while;
    return a,b;

end function;

