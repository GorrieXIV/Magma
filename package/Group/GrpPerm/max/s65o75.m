S65Identify := function(group, soc:max:=true,Print:=0)
  A := AutS65();
  ims := [A.1, A.2];
  S := sub< A | ims >;
  F, phi := FPGroupStrong(S);
  aut := Index(group, soc);
  if Print gt 1 then printf "group is S(6,5):[%o]\n", aut; end if;

  iss, gens := StandardGenerators(soc, "S65");
  if not iss then error "Standard generator error in S(6,5)"; end if;
  a:=gens[1]; b:=gens[2];
  soc := sub< soc | a,b >;
  newgens := [ group | a,b ];
  for g in Generators(group) do
     if not g in sub<group|newgens> then Append(~newgens,g); end if;
  end for;
  group := sub<group|newgens>;

  //we need to find images of generators in standard copy
  homom :=  hom< soc -> A | ims >;
  for i in [3..Ngens(group)] do
    g := group.i;
    conjim := homom(soc.1^g);
    isc, h := IsConjugate(A,A.1,conjim);
    if not isc then error "Conjugation error in Aut(S(6,5))"; end if;
    CG := Centraliser(A,conjim);
    ce := h;
    isc, h := IsConjugate(CG,A.2^ce,homom(soc.2^g));
    if not isc then error "Conjugation error in Aut(S(6,5))"; end if;
    ce := ce*h;
    Append(~ims,ce);
  end for;
  homom :=  hom< group -> A | ims >;
  imhom := Image(homom);

  maximals:= [];
  if not max then 
    return homom, A, maximals, F, phi;
  end if;
    
  //Now for the maximals.  We work down the table in the book
  //These are subgroups of Sp(6,5), so we need a homomorphism
  iss, gens := StandardGenerators(Sp(6,5),"S65":Projective:=true);
  sp := sub< GL(6,5) | gens >;
  sptoA := hom< sp->A | [A.1,A.2] >;

  //C1s
  Append(~maximals, sptoA(PointStabSp(6,5)));
  Append(~maximals, sptoA(IsotropicStabSp(6,5,2)));
  Append(~maximals, sptoA(IsotropicStabSp(6,5,3)));
  Append(~maximals, sptoA(SymplecticStab(6,5,2)));

  //C2s
  Append(~maximals, sptoA(GetWreathProd(6,5,3)));
  Append(~maximals, sptoA(GetStabOfHalf(6,5)));
  
  //C3s
  Append(~maximals, sptoA(GammaSp(6,5,3)));
  Append(~maximals, sptoA(GammaUMeetSp(6,5)));

  //C4
  Append(~maximals, sptoA(SpTensors(2,3,5)));

  //C9
  Append(~maximals, sptoA(J2()));

  return homom, A, maximals, F, phi;
end function;


