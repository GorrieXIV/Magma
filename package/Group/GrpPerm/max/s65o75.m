distinguish_o75 := function(G)
    // G is either s65 or o75, tell them apart 
    // return true of O75, false if S65
    local a, b, b2;
    P := RandomProcess(G);
    found_14 := false;
    repeat
	r := Random(P);
	ord := Order(r);
	if ord gt 78 then return false; /* proof it is s65 */ end if;
  //O75 has involution with centraliser divisible by 13 and 78, S65 does not
        if ord mod 62 eq 0 then
          r := r^(ord div 2);
          C := CentraliserOfInvolution(G,r);
          PP := RandomProcess(C);
          for i in [1..50] do
            if Order(Random(PP)) mod 13 eq 0 then
              return true;
            end if;
          end for;
	end if;
    until false;
end function;

S65O75Identify := function(group:max:=true,Print:=0)
