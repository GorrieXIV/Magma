get_standard_gens_tits := function(G)
    local	P, a, b, x, need_a, need_b;
    P := RandomProcess(G);
    need_a := true;
    need_b := true;
    repeat 
	x := Random(P);
	ord := Order(x);
	if need_a and ord in {10, 16} then
	    a := x ^ (ord div 2);
	    need_a := false;
	elif need_b and ord mod 3 eq 0 then
	    b := x ^ (ord div 3);
	    need_b := false;
	end if;
    until not (need_a or need_b);
    while Order(a*b) ne 13 do
	b := b ^ Random(P);
    end while;
    return a, b;
end function;

