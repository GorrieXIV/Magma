	// two copies of A6.2^2
	M := sub<simp|
	     b * a * b^-1 * a * b^-1 * a * b * a * b * a * b^-1, 
	     a * b^-1 * a * b^-1 * a * b^-1 * a * b^-1 * a * b * a * b^-1 * 
		a * b^-1 * a * b >;
	M`Order := 1440;
	Append(~maximals, M);
	Append(~maximals, M^c);

    else
	assert aut eq 2;

	// 2 classes of novelties from within the L(3,3):2's

	// 3^(1+2):8:2, intersection with simp is 3^(1+2):2^2.2
	M := sub<simp|
	    a * b * a * b^-1 * a * b * a * b * a * b * a * b * a * b^-1 * 
		a * b * a * b * a * b^-1 * a * b * a * b * a, 
	    a * b * a * b * a * b * a * b^-1 * a * b^-1 * a * 
		b * a * b^-1 * a * b^-1 * a * b * a * b^-1 * a * b^-1 * 
		a * b^-1 * a * b * a * b * a * b * a * b * a * b * a * b>;
	M`Order := 216;
	Append(~maximals, M);

	// 13:12, intersection with simp is 13:6
	M := sub<simp|
	    b * a * b * a * b^-1 * a * b * a * b * a * b^-1 * 
		a * b * a * b * a * b^-1 * a * b * a * b * a * b^-1 * a * 
		b^-1 * a * b^-1 * a * b * a * b^-1 * a * b^-1 * a * b^-1 * 
		a * b^-1 * a * b * a * b, 
	    b * a * b * a * b * a * b * a * b * a * b^-1 * a * 
		b * a * b * a * b * a * b * a * b^-1 * a * b * a * b * a * 
		b * a * b^-1 * a * b * a * b * a * b * a * b^-1 * a * b * 
		a * b^-1 * a * b^-1 * a * b * a * b >;
	M`Order := 78;
	Append(~maximals, M);

    end if;

    return homom, atits, maximals, F, phi;

