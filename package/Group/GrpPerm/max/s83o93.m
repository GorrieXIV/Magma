S83Identify := function(group, soc:max:=true,Print:=0)
  A := AutS83();
  ims := [A.1, A.2];
  S := sub< A | ims >;
  F, phi := FPGroupStrong(S);
  aut := Index(group, soc);
  if Print gt 1 then printf "group is S(8,3):[%o]\n", aut; end if;

  iss, gens := StandardGenerators(soc, "S83");
  if not iss then error "Standard generator error in S(8,3)"; end if;
  a:=gens[1]; b:=gens[2];
  soc := sub< soc | a,b >;
  newgens := [ group | a,b ];
  for g in Generators(group) do
     if not g in sub<group|newgens> then Append(~newgens,g); end if;
  end for;
  group := sub<group|newgens>;

  //we need to find images of generators in standard copy
  homom :=  hom< soc -> A | ims >;
  for i in [3..Ngens(group)] do
    g := group.i;
    conjim := homom(soc.1^g);
    isc, h := IsConjugate(A,A.1,conjim);
    if not isc then error "Conjugation error in Aut(S(8,3))"; end if;
    CG := Centraliser(A,conjim);
    ce := h;
    isc, h := IsConjugate(CG,A.2^ce,homom(soc.2^g));
    if not isc then error "Conjugation error in Aut(S(8,3))"; end if;
    ce := ce*h;
    Append(~ims,ce);
  end for;
  homom :=  hom< group -> A | ims >;
  imhom := Image(homom);

  maximals:= [];
  if not max then 
    return homom, A, maximals, F, phi;
  end if;
    
  //Now for the maximals.  We work down the table in the book
  //These are subgroups of Sp(8,3), so we need a homomorphism
  iss, gens := StandardGenerators(Sp(8,3),"S83":Projective:=true);
  sp := sub< GL(8,3) | gens >;
  sptoA := hom< sp->A | [A.1,A.2] >;

  //C1s
  Append(~maximals, sptoA(PointStabSp(8,3)));
  Append(~maximals, sptoA(IsotropicStabSp(8,3,2)));
  Append(~maximals, sptoA(IsotropicStabSp(8,3,3)));
  Append(~maximals, sptoA(IsotropicStabSp(8,3,4)));
  Append(~maximals, sptoA(SymplecticStab(8,3,2)));

  //C2s
  Append(~maximals, sptoA(GetWreathProd(8,3,4)));
  Append(~maximals, sptoA(GetWreathProd(8,3,2)));
  Append(~maximals, sptoA(GetStabOfHalf(8,3)));
  
  //C3s
  Append(~maximals, sptoA(GammaSp(8,3,2)));
  Append(~maximals, sptoA(GammaUMeetSp(8,3)));

  //C4
  _, G := SpTensors(2,4,3); //only want the GO^-(4,3) type
  Append(~maximals, sptoA(G));

  //C6
  Append(~maximals,
              sptoA(NormaliserOfExtraSpecialGroupMinus(8,3:symplectic:=true)));

  //C9
  Append(~maximals, sptoA(L2_27()));

  return homom, A, maximals, F, phi;
end function;


