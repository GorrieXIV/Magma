distinguish_o93 := function(G)
    // G is either s83 or o93, tell them apart 
    // return true of O93, false if S83
    local a, b, b2;
    P := RandomProcess(G);
    found_14 := false;
    repeat
	r := Random(P);
	ord := Order(r);
	if ord gt 60 then return false; /* proof it is s83 */ end if;
  //O93 has involution with centraliser divisible by 3^12, S83 does not
        if ord mod 26 eq 0 then
          r := r^(ord div 2);
          C := CentraliserOfInvolution(G,r);
          if #C mod 3^12 eq 0 then return true; end if; 
	end if;
    until false;
end function;

S83O93Identify := function(group:max:=true,Print:=0)
/* 
   group must be isomorphic to one of S(8,3), S(8,3).2, O(9,3) or O(9,3).2
   Work out which simple group it is and call the appropriate
   subroutine to complete the maximal subgroups stuff.
*/
    soc := Socle(group);
    o_flag := distinguish_o93(soc);
    if o_flag then 
	return O93Identify(group, soc:max := max, Print := Print);
    else
	return S83Identify(group, soc:max := max, Print := Print);
    end if;
end function;
