  newgens := [ group | a,b];
  for g in Generators(group) do
     if not g in sub<group|newgens> then Append(~newgens,g); end if;
  end for;
  group := sub<group|newgens>;
  //we need to find images of generators in standard copy
  homom :=  hom< soc -> A | ims >;
  for i in [3..Ngens(group)] do
    g := group.i;
    conjim := homom(soc.1^g);
    isc, h := IsConjugate(A,A.1,conjim);
    if not isc then error "Conjugation error in Aut(O^+(8,3))"; end if;
    CG := Centraliser(A,conjim);
    ce := h;
    isc, h := IsConjugate(CG,A.2^ce,homom(soc.2^g));
    if not isc then error "Conjugation error in Aut(O^+(8,3))"; end if;
    ce := ce*h;
    Append(~ims,ce);
  end for;
  homom :=  hom< group -> A | ims >;
  imhom := Image(homom);
  //identify as subgroup of S4
  s4, tos4 := quo< A | S >;
  if s4 ne Sym(4) then
    isit,f := IsIsomorphic(s4,Sym(4)); assert isit;
    s4 := Sym(4); tos4 := tos4*f;
  end if;
  ims4 := tos4(imhom);
  type :=
    #ims4 eq 1 select "om" else
    #ims4 eq 2 and IsConjugate(s4, ims4, sub<s4|(1,2)(3,4)>) select "so" else 
    #ims4 eq 2 select "om2" else 
    #ims4 eq 3 select "om3" else 
    #ims4 eq 4 and IsConjugate( s4, ims4, sub<s4|(1,2)(3,4),(1,3)(2,4)> )
                                                           select "omv4" else 
    #ims4 eq 4 and IsConjugate( s4, ims4, sub<s4|(1,2),(3,4)> )
                                                           select "go" else 
    #ims4 eq 4 select "omc4" else 
    #ims4 eq 6 select "oms3" else 
    #ims4 eq 8 select "cgo" else 
    #ims4 eq 12 select "oma4" else 
    "oms4";
  if Print gt 1 then print "type =", type; end if;

  maximals:= [];
  if not max then 
    return homom, A, maximals, F, phi;
  end if;

  //Now for the maximals.  We work down the table in the book
  //These are subgroups of OmegaPlus(8,3), so we need a homomorphism
  iss, gens := StandardGenerators(OmegaPlus(8,3),"O8p3":Projective:=true);
  om := sub< GL(8,3) | gens >;
  omtoA := hom< om->A | [A.1,A.2] >;
  
  //C1s
  if 8 mod #ims4 eq 0 then //c=3,stab S1=<delta',delta,gamma>
    G := omtoA(IsotropicStabOmega(8,3,1,1));//c=3,stab S1=<delta',delta,gamma>
    copies := [G, G^A.5, G^((A.5)^2)];
    if type in {"om","so","omv4"} then
      maximals cat:= copies;
    else
      //type in {"om2","go","omc4","cgo"} - have to ascertain which of three 
      //is normalised by imhom
      if IsConjugate(imhom, copies[2], copies[3]) then copy:=copies[1];
      elif IsConjugate(imhom, copies[1], copies[3]) then copy:=copies[2];
      else assert IsConjugate(imhom, copies[1], copies[2]); copy:=copies[3];
      end if;
      Append(~maximals, copy);
    end if;
  end if;

  if #ims4 mod 3 eq 0 then //N1, c=1, stab = S4
    Append(~maximals, omtoA(KlLine4(3)));
  end if;

  Append(~maximals, omtoA(IsotropicStabOmega(8,3,2,1)) ); //c=1

  if type in {"om2","go","omc4","cgo"} then
                         //N2, c=3, stab S1=<delta',delta,gamma>
    G :=  omtoA(KlLine7(3));
    copies := [G, G^A.5, G^((A.5)^2)];
    //have to ascertain which of three is normalised by imhom
    if IsConjugate(imhom, copies[2], copies[3]) then copy:=copies[1];
    elif IsConjugate(imhom, copies[1], copies[3]) then copy:=copies[2];
    else assert IsConjugate(imhom, copies[1], copies[2]); copy:=copies[3];
    end if;
    Append(~maximals, copy);
  end if;
  
  //next one is also C9
  if type in {"om","so","om2","go"} then //c=6, stab S3 = <delta',gamma>
    G := omtoA(NonDegenStabOmega(8,3,1,7,0)); //Omega_7(3)
    del := A.3^A.5;
    copies := IsConjugate(S,G,G^del) select
        [G, G^A.5, G^(A.5^2), G^A.3, G^(A.3*A.5), G^(A.3*A.5^2)] else
        [G, G^A.5, G^(A.5^2), G^del, G^(del*A.5), G^(del*A.5^2)];
    if type eq "om" then 
      maximals cat:= copies;
    else //have to find which two are normalised
      t := homom(group.3);
      ct := 0;
      for c in copies do
        if IsConjugate(S,c,c^t) then
          ct+:=1; Append(~maximals,c);
          if ct eq 2 then break; end if;
        end if;
      end for;
    end if;
  end if;

  if type in {"om","om2","om3","oms3"} and #ims4 mod 3 eq 0 then
                                          //N1, c=4, stab = <gamma,tau>
    G := omtoA(KlLine15(3)); //G2(3)
    del := A.3^A.5;
    copies := [G, G^A.3, G^del, G^(del*A.3)];
    //should be one normalised copy
    t := homom(group.3);
    if Ngens(group) gt 3 then u := homom(group.4); end if;
    ct := 0;
    for c in copies do
        if IsConjugate(S,c,c^t) and
                          (Ngens(group) eq 3 or IsConjugate(S,c,c^u)) then
          ct +:= 1; Append(~maximals,c); break;
        end if;
    end for;
    if ct ne 1 then error "Error in finding C1/C2 maximal"; end if;
  end if;
  
  //next one is also C2 type GL(4,3).2
  if type in {"omv4","omc4","cgo"} then
                                   //c=3,stab S1=<delta',delta,gamma>, N3 q=3
    G := omtoA(NonDegenStabOmega(8,3,1,6,1));//c=3,stab S1=<delta',delta,gamma>
    copies := [G, G^A.5, G^((A.5)^2)];
    if type eq "omv4" then
      maximals cat:= copies;
    else
      //have to ascertain which of three is normalised by imhom
      if IsConjugate(imhom, copies[2], copies[3]) then copy:=copies[1];
      elif IsConjugate(imhom, copies[1], copies[3]) then copy:=copies[2];
      else assert IsConjugate(imhom, copies[1], copies[2]); copy:=copies[3];
      end if;
      Append(~maximals, copy);
    end if;
  end if;

  if #ims4 ge 12 then //N4, c=1
    Append(~maximals, omtoA(KlLine22(3)));
  end if;

  //next one is also C3
  if 8 mod #ims4 eq 0 then //c=3,stab S1=<delta',delta,gamma>
    G := omtoA(NonDegenStabOmega(8,3,1,6,-1));//c=3,stab S1=<delta',delta,gamma>
    copies := [G, G^A.5, G^((A.5)^2)];
    if type in {"om","so","omv4"} then
      maximals cat:= copies;
    else
      //type in {"om2","go","omc4","cgo"} - have to ascertain which of three 
      //is normalised by imhom
      if IsConjugate(imhom, copies[2], copies[3]) then copy:=copies[1];
      elif IsConjugate(imhom, copies[1], copies[3]) then copy:=copies[2];
      else assert IsConjugate(imhom, copies[1], copies[2]); copy:=copies[3];
      end if;
      Append(~maximals, copy);
    end if;
  end if;

  if #ims4 mod 3 eq 0 then //N1, c=1
    Append(~maximals, omtoA(KlLine26(3)));
  end if;

  //next one is also C4
  if type in {"om","so","om2","go"} then //c=6, stab S3 = <delta',gamma>
    G := omtoA(NonDegenStabOmega(8,3,1,5,0)); //Omega_7(3)
    del := A.3^A.5;
    copies := IsConjugate(S,G,G^del) select
        [G, G^A.5, G^(A.5^2), G^A.3, G^(A.3*A.5), G^(A.3*A.5^2)] else
        [G, G^A.5, G^(A.5^2), G^del, G^(del*A.5), G^(del*A.5^2)];
    if type eq "om" then 
      maximals cat:= copies;
    else //have to find which two are normalised
      t := homom(group.3);
      ct := 0;
      for c in copies do
        if IsConjugate(S,c,c^t) then
          ct+:=1; Append(~maximals,c);
          if ct eq 2 then break; end if;
        end if;
      end for;
    end if;
  end if;

  //C2s follow - the first one is also C6
  if type in {"so","go"} then //N5, c=6, stab S3 = <delta',gamma>
    G := omtoA(OrthImprim(8,3,1,1,0)); //2^7A_8
    del := A.3^A.5;
    copies := IsConjugate(S,G,G^del) select
        [G, G^A.5, G^(A.5^2), G^A.3, G^(A.3*A.5), G^(A.3*A.5^2)] else
        [G, G^A.5, G^(A.5^2), G^del, G^(del*A.5), G^(del*A.5^2)];
    //have to find which two are normalised
    t := homom(group.3);
    ct := 0;
    for c in copies do
      if IsConjugate(S,c,c^t) then
        ct+:=1; Append(~maximals,c);
        if ct eq 2 then break; end if;
      end if;
    end for;
  end if;

  if type in {"om","om2","om3","oms3"} and #ims4 mod 3 eq 0 then
                                             //N1, c=4, stab = <gamma,tau>
    G := omtoA(KlLine51(3));
    del := A.3^A.5;
    copies := [G, G^A.3, G^del, G^(del*A.3)];
    //should be one normalised copy
    t := homom(group.3);
    if Ngens(group) gt 3 then u := homom(group.4); end if;
    ct := 0;
    for c in copies do
        if IsConjugate(S,c,c^t) and
                          (Ngens(group) eq 3 or IsConjugate(S,c,c^u)) then
          ct +:= 1; Append(~maximals,c); break;
        end if;
    end for;
    if ct ne 1 then error "Error in finding C2 maximal"; end if;
  end if;

  if type in {"omv4","go","omc4","cgo","oma4","oms4"} then //N6 if q=3, c=1
    G := omtoA(OrthImprim(8,3,1,2,-1));
    Append(~maximals,G);
  end if;

  G := omtoA(OrthImprim(8,3,1,4,1)); //c=1
  Append(~maximals,G);

  //next one is also C3
  if 8 mod #ims4 eq 0 then //c=3,stab S1=<delta',delta,gamma>
    G := omtoA(OrthImprim(8,3,1,4,-1));//c=3,stab S1=<delta',delta,gamma>
    copies := [G, G^A.5, G^((A.5)^2)];
    if type in {"om","so","omv4"} then
      maximals cat:= copies;
    else
      //type in {"om2","go","omc4","cgo"} - have to ascertain which of three 
      //is normalised by imhom
      if IsConjugate(imhom, copies[2], copies[3]) then copy:=copies[1];
      elif IsConjugate(imhom, copies[1], copies[3]) then copy:=copies[2];
      else assert IsConjugate(imhom, copies[1], copies[2]); copy:=copies[3];
      end if;
      Append(~maximals, copy);
    end if;
  end if;

  if #ims4 ge 12 then //N4, c=1
    Append(~maximals, omtoA(KlLine61(3)));
  end if;
   
  //no further C3,C4,C6, no C5,C7
  //one remaining C9: O8p2
  if type in {"om","om2","om3","oms3"} then //c=4, stab = <gamma,tau>
    G := omtoA(O8p2());
    del := A.3^A.5;
    copies := [G, G^A.3, G^del, G^(del*A.3)];
    if type eq "om" then
      maximals cat:= copies; 
    elif type eq "om2" then
      //should be two normalised copies
      t := homom(group.3);
      ct := 0;
      for c in copies do
        if IsConjugate(S,c,c^t) then
          ct+:=1; Append(~maximals,c);
          if ct eq 2 then break; end if;
        end if;
      end for;
      if ct ne 2 then error "Error in finding C9 maximal"; end if;
    else
      //one normalised copy
      t := homom(group.3);
      if Ngens(group) gt 3 then u := homom(group.4); end if;
      ct := 0;
      for c in copies do
        if IsConjugate(S,c,c^t) and
                          (Ngens(group) eq 3 or IsConjugate(S,c,c^u)) then
          ct +:= 1; Append(~maximals,c); break;
        end if;
      end for;
      if ct ne 1 then error "Error in finding C9 maximal"; end if;
    end if;
  end if;

  return homom, A, maximals, F, phi;
end function;
