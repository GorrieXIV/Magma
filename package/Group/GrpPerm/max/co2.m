/* code for maximals of Co2 */

get_standard_gens_co2 := function(P) 
/* P is a random process for a group isomorphic to co2 */
/* Use algorithm from Birmingham atlas */
    repeat
	x := Random(P);
	ord := Order(x);
    until ord in {16,18,28};
    a := x ^ (ord div 2);
    delete x;
    repeat
	repeat
	    y := Random(P);
	    ord := Order(y);
	until ord in {15,30};
	y := y ^ (ord div 5);
	count := 0;
	repeat
	    count +:= 1;
	    b := y^Random(P);
	    ord := Order(a*b);
	until count eq 35 or ord eq 28;
	if ord ne 28 then ord := 0; continue; end if;
	ord := Order(a*b^2);
	if ord eq 15 then continue; end if;
	assert ord eq 9;
    until ord eq 9;
    delete y;
    return a, b;
end function;

