get_standard_gens_Ru := function(G)
    /* G is a group iso to Ru */
    /* standard gens as defined in B'ham Atlas */
    /* coded by Bill Unger, 3/9/200r73 */

    P := RandomProcess(G);
    got_a := false;
    got_b := false;
    repeat
	x := Random(P);
	ord := Order(x);
	if not got_a and ord in {14, 26} then // 15 in 91
	    a := x^(ord div 2);
	    got_a := true;
	    continue;
	end if;
	if not got_b and ord eq 24 then // about 1 in 12
	    b := x ^ 6;
	    got_b := true;
	    continue;
	end if;
    until got_a and got_b;

    /* get a good b */
    repeat
        b := b^Random(P);
    until Order(a*b) eq 13; // about 1 in 163
    return a, b;

end function;

