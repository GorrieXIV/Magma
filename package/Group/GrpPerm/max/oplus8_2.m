function GetOuterInvol(group, soc)
  start:= false;
  while not start do
    x:= Random(group);
    if not x in soc then start:= true; end if;
  end while;
  finished:= false;
  while not finished do
    y:= Random(soc);
    if Order(x*y) eq 2 then
      return x*y;
    end if;
  end while;
end function;

/*************************************************************************/

function MakeCopies(subgroup,three_cyc)
  return [subgroup, subgroup^three_cyc, subgroup^(three_cyc^2)]; 
end function;

/**************************************************************************/

function SelectGroup(group, grps)
  assert #grps eq 3;
  if IsConjugate(group, grps[1], grps[2]) then
    return grps[3];
  elif IsConjugate(group, grps[1], grps[3]) then
    return grps[2];
  else
    assert IsConjugate(group, grps[2], grps[3]);
    return grps[1];
  end if;
end function;

