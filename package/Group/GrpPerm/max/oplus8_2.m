//This finds the standard generators a and b that are described in Rob
//Wilson's online ATLAS.

function FindGoodGens(group)
  founda:= false;
  while not founda do
    a:= Random(group);
    if Order(a) eq 2 then
      if #Centraliser(group, a) eq 3072 then
        founda:= true;
      end if;
    end if;
  end while;
  found_firstb:= false;
  while not found_firstb do
    first_b:= Random(group);
    if Order(first_b) eq 5 then
      found_firstb:= true;
    end if;
  end while;
  foundb:= false;
  while not foundb do
    x:= Random(group);
    b:= first_b^x;
    if Order(a* b^2) eq 15 and Order(a*b) eq 12 and 
      Order((a*b)^4*b*(a*b)^2*b*a*b^2) eq 8 then
      if #Centraliser(group, a*b) eq 24 then
        foundb:= true;
      end if;
    end if;
  end while;
  return a, b;
end function;

