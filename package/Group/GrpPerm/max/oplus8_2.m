function WhichGroup(group)
  o:= Order(group);
  order_pomega:= #POmegaPlus(8, 2);
  if o eq order_pomega then type:= "omega"; soc:= group;
  elif o eq 2*order_pomega then type:= "pso"; soc:= Socle(group);
  elif o eq 3*order_pomega then type:= "omega.3"; soc:= Socle(group);
  else assert o eq 6*order_pomega; 
    type:= "aut";
    soc:= Socle(group);
  end if;
  return soc, type;
end function;

