function OPlus8_2Identify(group: max:= true, Print:=0)
  osimp, three_cyc, invol, isotrop_line, sp62, isotrop_point,
           minus_line, imp_orth_two, imp_orth_four, a9, maximal_isotrop, u3_3,
           extra_spec3, five_squared, two_grp := GetGroups();
  F, phi := FPGroupStrong(osimp);
  soc, type:= WhichGroup(group);
  if Print gt 1 then print "type =", type; end if;
  a, b:= FindGoodGens(soc);
  soc := sub< soc | a,b >;
  newgens := [ group | a,b];
  for g in Generators(group) do
     if not g in sub<group|newgens> then Append(~newgens,g); end if;
  end for;
  group := sub<group|newgens>;
  aop82 := sub< Sym(405) | osimp.1, osimp.2, three_cyc, invol >;
  ims := [ aop82 | osimp.1, osimp.2 ];
  homom :=  hom< soc -> osimp | ims >;
  for i in [Ngens(soc)+1..Ngens(group)] do
      g := group.i;
      CG := aop82; ce := Id(aop82);
      for j in [1..2] do
        isc, h := IsConjugate(CG,osimp.j^ce,homom(soc.j^g));
        if not isc then error "Conjugation error in Aut(O^+(8,2))"; end if;
        CG := Centraliser(CG,homom(soc.j^g));
        ce := ce*h;
      end for;
      Append(~ims,ce);
  end for;
  newgens := ims;
  for g in Generators(aop82) do
     if not g in sub<aop82|ims> then Append(~ims,g); end if;
  end for;
  aop82 := sub< Sym(405) | ims >;
  homom :=  hom< group -> aop82 | newgens >;

  maximals:= [];
  if not max then 
    return homom, aop82, maximals, F, phi;
  end if;

  Append(~maximals, isotrop_line);
  Append(~maximals, imp_orth_two);

  if type in {"omega", "pso"} then
    for subgroup in [sp62, isotrop_point, minus_line, imp_orth_four, a9]
    do
      grps:= MakeCopies(subgroup,three_cyc);
      if type eq "omega" then
        maximals:= maximals cat grps;
      elif type eq "pso" then
        Append(~maximals, SelectGroup(Image(homom),grps)); 
      end if;
    end for;
    if type eq "pso" then
      grps:= MakeCopies(maximal_isotrop,three_cyc);
      Append(~maximals,  SelectGroup(Image(homom),grps));
    end if;
    return homom, aop82, maximals, F, phi;
  end if;

  assert type in {"omega.3", "aut"};

  Append(~maximals, u3_3);
  Append(~maximals, extra_spec3);
  Append(~maximals, five_squared);
  Append(~maximals, two_grp);

  return homom, aop82, maximals, F, phi;
end function;
