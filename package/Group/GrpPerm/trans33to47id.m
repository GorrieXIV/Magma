TransIdent := function(G)
/* G should be a transitive permutation group of degree d in the range [33..47]
 * and L should be a list containing a unique representative of the conjugacy
 * classes in S_d of all such groups satisfying the following conventions.
 * The groups in L must be ordered first by increasing group order.
 * For groups of a fixed order ord, the primitive groups come last, in
 * the same order as the groups of order ord in the primitive groups database.
 * The imprimitive groups in L of order ord are ordered in increasing order of
 * minimal blocksize mb. Each such group must preserve the block system
 * { {1,2,..,mb}, {mb+1,...,2*mb}, ... }
 * (although it may preserve other block systems as well), and the transitive
 * action of the group in L on this block system must be a group in the
 * list TransitiveGroup(d/mb,i). The groups in L of order ord and minimal
 * blocksize mb are in increasing order of i, with all groups having the
 * same kernel on this block system coming together.  
 *
 * TransIdent returns the unique position pos of a group in L together
 * with an element g in S_d with G^d = L[pos]
 */
  local d, trip, ord, ngps, mb, nb, bs,pos, minpos, maxpos, MP, MPp,
        newminpos, newmaxpos,
        phi, P, K, Pp, Kp, NP, NK, badkers, gotker, KK, GG, GGG,
        g, g1, g2, g3, g4;
  d := Degree(G);
  assert IsTransitive(G) and d in [33..47];
  L := TransitiveGroupDatabase(d);
  ord := #G;
  ngps := #L;
  //identify positions of groups of order ord in L
  //we do this from the stored data
  trip := [ x: x in TransGpOrdData[d] | x[1] eq ord ][1];
  minpos:=trip[2]; maxpos:=trip[3];
  
/*
  minpos := 0; maxpos := 0;
  for i in [1..ngps] do
    if minpos eq 0 and #L[i] eq ord then minpos := i; end if;
    if #L[i] gt ord then
      maxpos := i-1; break;
    end if;
  end for;
  assert minpos ne 0;
  if maxpos eq 0 then maxpos := ngps; end if;
*/

  //deal with primitive groups first
  if IsPrimitive(G) then
    for i in [minpos..maxpos] do
      if IsPrimitive(L[i]) then minpos := i; break; end if;
    end for;
    for pos in [minpos..maxpos] do
      isc, g :=  IsConjugate(Sym(d),G,L[pos]);
      if isc then return pos, g; end if;
    end for;
  end if;

  //now the imprimitive case - find minimal blocks
  MP := MinimalPartitions(G);
  mb := Min( [ #m[1] : m in MP ] );
  nb := d div mb;
  bs := {@ { (k-1)*mb+1 .. k*mb } : k in [1..nb] @};
  MP := [ b : b in MP | #b[1] eq mb ];
  //locate positions of candidate groups in L
  found := false;
  for pos in [minpos..maxpos] do
    MPp := MinimalPartitions(L[pos]);
    if MPp eq [] then
      maxpos := pos-1; break;
    end if;
    if Min( [#m[1] : m in MPp] ) eq mb then
      if not found then minpos := pos; found := true; end if;
    elif found then
      maxpos := pos-1; break;
    end if;
  end for;
  assert found;
  //We have to try each block system in MP in turn and conjugate them to the
  //block system bs preserved by G
  
  for b in MP do
    isc, g1 := IsConjugate(Sym(d),b,bs); assert isc;
    GG := G^g1;
    phi, P, K := BlocksAction(GG,bs);
    //need to identify group P of degree nb in database
    apos, _, g2 := TransitiveGroupIdentification(P : Raw:=false);
    //pull g2 back to Sym(d) and conjugate GG
    g2 := Sym(d)![ (k^g2 - 1)*mb + i : i in [1..mb], k in [1..nb] ];
    GG := GG^g2;
    phi, P, K := BlocksAction(GG,bs);
    assert P eq TransitiveGroup(nb,apos);
    //locate positions of candidate groups in L
    found := false; newmaxpos := maxpos;
    for pos in [minpos..maxpos] do
      Pp := BlocksImage(L[pos],bs);
      if Pp eq P then
        if not found then newminpos := pos; found := true; end if;
      elif found then
        newmaxpos := pos-1; break;
      end if;
    end for;
    if not found then continue b; end if;
    //now conjugate kernel and then whole group.
    NP := WreathProduct(Sym(mb), Normaliser(Sym(nb),P) );
    badkers := {}; //keep seen kernels
    gotker := false;
    KK := Sym(d); //anything that cannot be equal to a kernel!
    //conj class info can help in hard cases
    if newmaxpos - newminpos gt 50 then
      cs := [ <c[1],c[2]> : c in Classes(G) ];
    end if;
    for pos in [newminpos..newmaxpos] do
      Kp := BlocksKernel(L[pos],bs);
      if Kp in badkers then continue; end if;
      if not gotker or KK ne Kp then
        isc, g3 := IsConjugate(NP, K, Kp);
        if isc then
           GGG := GG^g3;
           KK := Kp;
           NK := Normaliser(NP,KK);
           gotker := true;
        else
          Include(~badkers,Kp);
          gotker := false;
          KK := Sym(d);
        end if;
      end if;
      if KK eq Kp then
        if newmaxpos - newminpos le 50 or
                       cs eq [ <c[1],c[2]> : c in Classes(L[pos]) ] then
          isc, g4 := IsConjugate(NK, GGG, L[pos]);
          if isc then return pos, g1*g2*g3*g4; end if;
        end if;
      end if;
    end for;
  end for;
end function;


intrinsic Trans33to47Identify(G::GrpPerm) -> RngIntElt, GrpPermElt
{Return identifying information for a transitive permutation group of
degree 33 to 47, and a conjugating element}

    require Degree(G) in [33..47] and IsTransitive(G):
	"Group must be transitive of degree 33 to 47 (inclusive)";

    return TransIdent(G);
end intrinsic;
