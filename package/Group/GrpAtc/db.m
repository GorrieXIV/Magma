LIBNAME := "GrpAtc";
B := 256;
MAX_INDEX := 5800;

read_index := function(path)
    F := Open(path, "r");
    size := 23204;	// temporary hack
    ibytes := ReadBytes(F, size);
    index := [ Seqint(ibytes[[k-3 .. k]], B) : k in [4 .. #ibytes by 4] ];
    return index;
end function;

index_data := function(path, n)
    F := Open(path, "r");
    Seek(F, 4*(n - 1), 0);
    ibytes := ReadBytes(F, 8);
    dstart := Seqint(ibytes[[1..4]], B);
    dend := Seqint(ibytes[[5..8]], B);
    return dstart, dend;
end function;

parse_word := function(data, pos, G)
    n := Ngens(G);
    nletters := data[pos];
    pos +:= 1;
    bits_per_letter := 1 + Ceiling(Log(2, n));
    nbytes := Ceiling(nletters * bits_per_letter / 8);
    ldata := Seqint(data[[pos .. pos + nbytes - 1]], B);
    pos +:= nbytes;
    phi := func<x | x lt n select x + 1 else n - x - 1>;
    letters := [ phi(x) : x in Intseq(ldata, 2^bits_per_letter, nletters) ];
    w := G!letters;
    return w,pos;
end function;

parse_grp_fp_quo := function(data, pos)
    n := data[pos];
    pos +:= 1;
    nrels := data[pos];
    pos +:= 1;
    F := FreeGroup(n);
    words := [];
    for i in [1..nrels] do
	words[i],pos := parse_word(data, pos, F);
    end for;
    id := Id(F);
    G := quo<F | [ w = id : w in words ]>;
    return G,pos;
end function;

parse_seq := function(data, pos, size, nbytes)
    S := [];
    for i in [1..size] do
	S[i] := Seqint(data[[pos .. pos + nbytes - 1]], B);
	pos +:= nbytes;
    end for;
    return S,pos;
end function;

parse_seq_seq := function(data, pos, len, size, nbytes)
    S := [];
    for i in [1..len] do
	S[i],pos := parse_seq(data, pos, size, nbytes);
    end for;
    return S,pos;
end function;

parse_wa := function(data, pos, G)
    n := Ngens(G);
    size := 2*n;
    len := Seqint(data[[pos .. pos + 3]], B);
    pos +:= 4;
    nbytes := Ceiling(Log(B, len + 1));
    WA,pos := parse_seq_seq(data, pos, len, size, nbytes);
    return WA, pos;
end function;

parse_d2 := function(data, pos, G)
    n := Ngens(G);
    size := (2*n + 1)^2 - 1;
    len := Seqint(data[[pos .. pos + 3]], B);
    pos +:= 4;
    nbytes := Ceiling(Log(B, len + 1));
    D2,pos := parse_seq_seq(data, pos, len, size, nbytes);
    return D2, pos;
end function;

parse_order := function(data, pos, G)
    return -1, pos;
end function;

parse_L := function(data, pos, G)
    len := Seqint(data[[pos .. pos + 3]], B);
    pos +:= 4;
    L := [];
    for i in [1..len] do
	w,pos := parse_word(data, pos, G);
	L[i] := Eltseq(w);
    end for;
    return L,pos;
end function;

read_atc := function(path, dstart, dend)
    F := Open(path, "r");
    Seek(F, dstart, 0);
    data := ReadBytes(F, dend - dstart);
    pos := 1;
    G,pos := parse_grp_fp_quo(data, pos);
    WA,pos := parse_wa(data, pos, G);
    D2,pos := parse_d2(data, pos, G);
    order,pos := parse_order(data, pos, G);
    L,pos := parse_L(data, pos, G);
    n := Ngens(G);
    A := &cat [ [k,-k] : k in [1..n] ];
    if #L eq 0 then
	return AutomaticGroup(G, A, WA, D2, order);
    else
	return AutomaticGroup(G, A, WA, D2, order, L);
    end if;
end function;

paths := function()
    dir := Sprintf("%o/data", GetLibraryRoot());
    datpath := Sprintf("%o/%o.dat", dir, LIBNAME);
    indpath := Sprintf("%o/%o.ind", dir, LIBNAME);

    return datpath, indpath;
end function;

intrinsic AutomaticGroup(n::RngIntElt) -> GrpAtc
{The automatic group corresponding to the n-th hyperbolic manifold
in the Hodgson-Weeks census}
    require n ge 0 and n le MAX_INDEX: "Invalid automatic group number";

    datpath, indpath := paths();
    dstart,dend := index_data(indpath, n);
    if dstart eq dend then
	error Sprintf("Automatic group %o is not yet in the database", n);
    end if;
    G := read_atc(datpath, dstart, dend);
    return G;
end intrinsic;

intrinsic AutomaticGroupIndices() -> SeqEnum[RngIntElt]
{The sequence of valid indices for groups in the automatic group database}
    datpath, indpath := paths();
    index := read_index(indpath);
    return [ n : n in [1 .. #index - 1] | index[n] ne index[n+1] ];
end intrinsic;


