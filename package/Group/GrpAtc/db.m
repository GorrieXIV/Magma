intrinsic AutomaticGroup(name::MonStgElt) -> GrpAtc
{The automatic group corresponding to named hyperbolic manifold
in the Hodgson-Weeks census}
    n := name_to_index(name);
    require n ge 0: "Invalid manifold name";

    datpath, indpath := paths();
    dstart,dend := index_data(indpath, n);
    if dstart eq dend then
	error Sprintf("Automatic group %o is not yet in the database", name);
    end if;
    G := read_atc(datpath, dstart, dend);
    return G;
end intrinsic;

intrinsic AutomaticGroup(r::Rec) -> GrpAtc
{The automatic group corresponding to the manifold record r}
    return AutomaticGroup(r`Name);
end intrinsic;

intrinsic AutomaticGroupNames() -> SeqEnum[MonStgElt]
{The sequence of valid names for groups in the automatic group database}
    return Setseq(man_names[AutomaticGroupIndices()]);
end intrinsic;

