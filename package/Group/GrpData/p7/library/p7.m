intrinsic SearchPGroups(p::RngIntElt, n::RngIntElt : Rank := 0, Class := 0,
    Select := func<x|true>, Limit := 0) -> []
{Search for groups of order p^n satisfying the given Frattini Rank, pClass
and selection conditions}
    require IsPrime(p): "First argument must be prime";
    require n le 7 or p eq 2 and n le 9: "Order is unavailable";

    if Rank cmpeq 0 then Rank := [1..n]; end if;
    if Type(Rank) eq RngIntElt then Rank := [Rank]; end if;

    if Class cmpeq 0 then Class := [1..n]; end if;
    if Type(Class) eq RngIntElt then Class := [Class]; end if;

    if n eq 7 and p ge 3 then
	return Groupsp7(p: Rank := Rank, Class := Class, Select := Select,
	    Process := false, Limit := Limit);
    end if;

    /* Default case */
    P := SmallGroupProcess(p^n);
    res := [];
    limited := Limit ge 1;
    while not IsEmpty(P) do
	G := Current(P);
	if FrattiniQuotientRank(G) in Rank and pClass(G) in Class 
		and Select(G) then
	    Append(~res, G);
	    if limited and #res ge Limit then
		break;
	    end if;
	end if;
	Advance(~P);
    end while;
    return res;
end intrinsic;



